package com.example.bca;

public class UserData {
    private String userEmail;
    private String userPassword;
    private String userFirstName;
    private String userLastName;
    private String userMonthlyBudget;
    private String userDate;
    private String userTime;
    private String userContact;
    private String userIDOriginal;

    public String getUserIDOriginal() {
        return userIDOriginal;
    }

    public void setUserIDOriginal(String userIDOriginal) {
        this.userIDOriginal = userIDOriginal;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserFirstName() {
        return userFirstName;
    }

    public void setUserFirstName(String userFirstName) {
        this.userFirstName = userFirstName;
    }

    public String getUserLastName() {
        return userLastName;
    }

    public void setUserLastName(String userLastName) {
        this.userLastName = userLastName;
    }

    public String getUserMonthlyBudget() {
        return userMonthlyBudget;
    }

    public void setUserMonthlyBudget(String userMonthlyBudget) {
        this.userMonthlyBudget = userMonthlyBudget;
    }

    public String getUserDate() {
        return userDate;
    }

    public void setUserDate(String userDate) {
        this.userDate = userDate;
    }

    public String getUserTime() {
        return userTime;
    }

    public void setUserTime(String userTime) {
        this.userTime = userTime;
    }

    public String getUserContact() {
        return userContact;
    }

    public void setUserContact(String userContact) {
        this.userContact = userContact;
    }
}